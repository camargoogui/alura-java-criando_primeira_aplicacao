package Exercicios;// Crie um programa que realize a média de duas notas decimais e exiba o resultado.

public class Media {
    public static void main(String[] args) {
        double nota1 = 8.3;
        double nota2 = 9.4;
        double media = (nota1 + nota2) /2;
        System.out.println("Média: " + media);
    }
}
